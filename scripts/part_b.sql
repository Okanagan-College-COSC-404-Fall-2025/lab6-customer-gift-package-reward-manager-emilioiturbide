-- Create the CUSTOMER_REWARDS table
CREATE TABLE CUSTOMER_REWARDS (
    REWARD_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CUSTOMER_EMAIL VARCHAR2(255) NOT NULL,
    GIFT_ID NUMBER,
    REWARD_DATE DATE DEFAULT SYSDATE,
    CONSTRAINT FK_GIFT_ID FOREIGN KEY (GIFT_ID) REFERENCES GIFT_CATALOG(GIFT_ID),
    CONSTRAINT FK_CUSTOMER_EMAIL FOREIGN KEY (CUSTOMER_EMAIL) REFERENCES CUSTOMERS(EMAIL_ADDRESS)
);
COMMIT;